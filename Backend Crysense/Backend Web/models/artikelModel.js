const mongoose = require("mongoose");

const artikelSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    imageUrl: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const artikel = mongoose.model("artikellist", artikelSchema);

module.exports = artikel;
